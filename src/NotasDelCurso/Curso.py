class Curso:

    """# ----------------------------------------
    # Constructor
    -----------------------------------------#"""

    def __init__(notas = int):
        notas = []

    def CalcularCantidadAprobados(self):
        aprobados = 0
        indice = 0

        while indice < len(notas):
            notas[indice] >= 3.0
            indice += 1

            return aprobados